# delete empty files
#-------------------------------------------------------------------------------
dir="/home/caltrop/WinXP/Garmin"
#-------------------------------------------------------------------------------
title-80.sh -t line "Deleting Empty WPT Files"
find "$dir/wpt" -type f -iname "*.wpt" | sort |
while read src
do
   n=$(cat "$src" | wc -l)
   if ((n<=10))
   then
      printf "%s %2d %s\n" "DELETE" "$n" "${src##*/}"
      rm "$src"
   # else
      # printf "%s %2d %s\n" "------" "$n" "${src##*/}"
   fi
done
